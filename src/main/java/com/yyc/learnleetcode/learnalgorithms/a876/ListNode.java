package com.yyc.learnleetcode.learnalgorithms.a876;

/**
 * Definition for singly-linked list.
 *
 * @author yuechao
 */
public class ListNode {

  int val;

  ListNode next;

  public ListNode() {
  }

  public ListNode(int val) {
    this.val = val;
  }

  public ListNode(int val, ListNode next) {
    this.val = val;
    this.next = next;
  }
}
