package com.yyc.learnleetcode.learnalgorithms.a938;

/**
 * Definition for a binary tree node.
 *
 * @author yuechao
 */
public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    public TreeNode() {
    }

    public TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
